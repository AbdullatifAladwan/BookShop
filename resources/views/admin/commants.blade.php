<script>
  // Listen for click event on "Add post" link
  document.getElementById("addPostLink").addEventListener("click", function() {
    // Show the modal
    var addPostModal = new bootstrap.Modal(document.getElementById("addPostModal"));
    addPostModal.show();
  });

  // Listen for click event on "Submit" button in the modal form
  document.getElementById("submitPostBtn").addEventListener("click", function() {
    // Retrieve form data
    var title = document.getElementById("title").value;
    var description = document.getElementById("description").value;
    var photo = document.getElementById("photo").files[0]; // Get the selected photo file

    // Create FormData object to send form data
    var formData = new FormData();
    formData.append('title', title);
    formData.append('description', description);
    formData.append('photo', photo);
    formData.append('_token', document.getElementsByName("_token")[0].value);

    // Send Ajax request using jQuery
    $.ajax({
      url: '/storepost',
      type: 'POST',
      data: formData,
      processData: false,
      contentType: false,
      success: function(response) {
        // Handle success response
        console.log(response);
        // Close the modal
        var addPostModal = bootstrap.Modal.getInstance(document.getElementById("addPostModal"));
        addPostModal.hide();
        // Reload the page or update the UI as needed
        location.reload(); // Example: Reload the page
      },
      error: function(xhr, status, error) {
        // Handle error response
        console.error(xhr.responseText);
      }
    });
  });
</script>

